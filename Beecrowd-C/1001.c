#include <stdio.h>

int main() {
    // Declaração de variáveis
    int A, B, soma;

    // Solicita ao usuário que insira os valores de A e B
    scanf("%d", &A);
    scanf("%d", &B);

    // Calcula a soma das variáveis A e B
    soma = A + B;

    // Exibe o resultado da soma das variáveis
    printf("X = %d\n", soma);

    return 0;
}